    private void cargarTabla() {

        Administrar_Personas per = new Administrar_Personas();
        DefaultTableModel modelo = new DefaultTableModel() {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable1.setModel(modelo);
        //.:Nombres de columnas:.

        modelo.addColumn("RUT");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDOS");
        modelo.addColumn("SEXO");

        modelo.addColumn("TELEFONO");
        modelo.addColumn("CORREO");
        TableColumnModel columnModel = jTable1.getColumnModel();
            columnModel.getColumn(0).setPreferredWidth(140);
            columnModel.getColumn(1).setPreferredWidth(75);
            columnModel.getColumn(2).setPreferredWidth(140);
            columnModel.getColumn(3).setPreferredWidth(140);
            columnModel.getColumn(4).setPreferredWidth(110);
            columnModel.getColumn(5).setPreferredWidth(160);

        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        

        ArrayList<Persona> listaPersonas = per.alertaLicencias();

        Object[] fila = new Object[10];
        int num = listaPersonas.size();
        for (int i = 0; i < num; i++) {

            fila[0] = listaPersonas.get(i).getRut();
            fila[1] = listaPersonas.get(i).getNombre();
            fila[2] = listaPersonas.get(i).getApellidos();

            char sex = listaPersonas.get(i).getSexo();
            String sexo = "" + sex;
            if (sexo.equals("F")) {
                fila[3] = "Femenino";
            } else {
                fila[3] = "Masculino";
            }

            fila[4] = listaPersonas.get(i).getTelefono();
            fila[5] = listaPersonas.get(i).getCorreo();

            modelo.addRow(fila);
        }

    }

